	public Animation GetAnimationFromSet( Animation baseAnim, int side, int sets=1 ){
		
		int baseEnumVal = (int)baseAnim;
		int set = Random.Range( 0, sets );
		Loli.Animation anim = (Loli.Animation)( baseEnumVal+set*2+side );
		if( currentAnim == anim ){
			//alternate animation so it always replay a new poke animation
			set = ++set%sets;
			anim = (Loli.Animation)( baseEnumVal+set*2+side );
		}
		return anim;
	}

	public Animation GetAnimationFromMood( Animation baseAnim ){
		return GetAnimationFromSet( baseAnim, System.Convert.ToInt32( !IsHappy() ) );
	}
	
